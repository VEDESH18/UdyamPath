export function filterSchemes(startup) {
  if (!startup?.sector && !startup?.stage && !startup?.budget) {
    return GOVERNMENT_SCHEMES.slice(0, 4);
  }

  return GOVERNMENT_SCHEMES.filter(scheme => {
    const sectorMatch = !startup.sector || scheme.sector.some(s => s.toLowerCase().includes(startup.sector.toLowerCase()) || startup.sector.toLowerCase().includes(s.toLowerCase()));
    const stageMatch = !startup.stage || scheme.stage.some(s => s.toLowerCase().includes(startup.stage.toLowerCase()) || startup.stage.toLowerCase().includes(s.toLowerCase()));
    const budgetMatch = !startup.budget || scheme.budget.some(b => b === startup.budget);
    return (sectorMatch || stageMatch || budgetMatch);
  }).slice(0, 6);
}
